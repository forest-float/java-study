public class Hello{
	//类名称   一个JAVA程序中只有一个public 类，但是可以有多个非public类
	//public类的类名称必须与该JAVA文件的文件名一致    非public类的类名则随意
	public static void main(String args[]){
		//主方法main，表示一个程序的起点，主方法所在的类叫主类
		System.out.println("Hello World!");//系统输出  Println   输出后加换行
		System.out.print("again");// print  输出后不换行
		System.out.println("");
		System.out.println("*************************");//系统输出  Println   输出后加换行
		System.out.println("*****java design*********");//系统输出  Println   输出后加换行
		System.out.println("*************************");//系统输出  Println   输出后加换行
	}	
}